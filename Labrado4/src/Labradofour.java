     void getcash(){
         String val1 = jTextField1.getText(); /*quantity*/
         String val2 = jLabel2.getText(); /*price*/
         String val3 = jTextField2.getText(); /*cash*/
         
         
         if (val1.equals("")){
             
             jTextField3.setText("");
         }
         else {
             Double num1 = Double.parseDouble (val1);
             Double price = Double.parseDouble(val2);
             Double toquan = Double.parseDouble(val3);
             Double overall = num1*price;
             Double change = toquan - overall;
             jTextField3.setText(change.toString());
         }
     }
     
