    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
        // TODO add your handling code here:
        
        int item = jComboBox1.getSelectedIndex();
         
         if (item == 0) {
             setproduct("Apple");
             setprice(300d);
            
         }
         
         else if (item ==1) {
             
            setproduct("Banana");
            setprice(350d);
  
         }
         
         else if (item ==2){
             
             setproduct("Eggplant");
             setprice(300d);
            
         }
         else if (item ==3){
             setproduct("Mango");
             setprice(500d);

         }
         
         else if (item ==4 ){
             setproduct("Pineapple");
             setprice(450d);
         }
         
         else {
             setproduct("");
             setprice(0d);
         }
         jLabel1.setText(jComboBox1.getSelectedItem().toString());
         setprice(getprice());
         jLabel2.setText(getprice().toString());
    }//GEN-LAST:event_jComboBox1ItemStateChanged
    
         
         
         
     
     
